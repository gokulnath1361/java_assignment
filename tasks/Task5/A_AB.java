package tasks.Task5;

public class A_AB {
    public static void main(String[]args){
        for(int i=1;i<=5;i++){
            char l='A';
            for(int j=1;j<=i;j++)
            {
                 System.out.print(l+" ");
                 l++;   
            };
            System.out.println();
        };
    }
}
